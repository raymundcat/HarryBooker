import UIKit

public extension UIColor {
    
    class var background: UIColor? {
        return UIColor(named: "Background")
    }
    
    class var text: UIColor? {
        return UIColor(named: "Text")
    }
    
    class var subText: UIColor? {
        return UIColor(named: "SubText")
    }
}
